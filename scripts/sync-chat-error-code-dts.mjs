import { readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const projectRoot = resolve(import.meta.dirname, "..");
const sourcePath = resolve(projectRoot, "shared", "chat-error-code.js");
const declarationPath = resolve(projectRoot, "shared", "chat-error-code.d.ts");

function parseErrorCodes(source) {
  const arrayMatch = source.match(
    /export\s+const\s+CHAT_ERROR_CODES\s*=\s*\[([\s\S]*?)\];/m,
  );
  if (!arrayMatch) {
    throw new Error(
      "CHAT_ERROR_CODES array was not found in shared/chat-error-code.js",
    );
  }

  const values = [];
  const valueRegex = /"([A-Z_]+)"/g;
  for (const match of arrayMatch[1].matchAll(valueRegex)) {
    values.push(match[1]);
  }

  if (values.length === 0) {
    throw new Error("CHAT_ERROR_CODES did not contain any string literals");
  }

  return values;
}

function toDeclaration(values) {
  const rows = values.map((value) => `  "${value}",`).join("\n");
  return `// Auto-generated by scripts/sync-chat-error-code-dts.mjs
// Source of truth: shared/chat-error-code.js

export declare const CHAT_ERROR_CODES: readonly [
${rows}
];

export type ChatErrorCode = (typeof CHAT_ERROR_CODES)[number];

export declare function isChatErrorCode(value: unknown): value is ChatErrorCode;
`;
}

async function main() {
  const source = await readFile(sourcePath, "utf8");
  const values = parseErrorCodes(source);
  const declaration = toDeclaration(values);
  await writeFile(declarationPath, declaration, "utf8");
  console.log(`[sync-chat-error-code-dts] Updated ${declarationPath}`);
}

await main();
